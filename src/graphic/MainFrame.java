    private int computeSizeWithMap(HashMap<String, ArrayList<VEvent>> map) {
        Set set = map.keySet();
        Iterator it = set.iterator();
        int nb = 0;
        while (it.hasNext()) {
            nb++;
            String cle = (String) it.next();
            ArrayList<VEvent> listeTmp = map.get(cle);
            nb += listeTmp.size();
        }
        return nb;
    }
    
    private void setTableWithEvents(ArrayList<VEvent> listEvents) {
        SimpleDateFormat ft = new SimpleDateFormat("'Le' dd.MM.yyyy");
        SimpleDateFormat ft2 = new SimpleDateFormat("HH:mm");

        ArrayList<String> listDates = getDates(listEvents);
        HashMap<String, ArrayList<VEvent>> map = convertListToMap(listEvents);
        
        Object[][] obj = new Object[computeSizeWithMap(map)][6];
        int i = 0;
        
        for(String date : listDates) {
            obj[i][0] = "Le " + date;
            i++;
            ArrayList<VEvent> listeTmp = map.get(date);
            for (VEvent event : listeTmp) {
                obj[i][1] = ft2.format(event.getDateStart().getValue());
                obj[i][2] = ft2.format(event.getDateEnd().getValue());
                obj[i][3] = event.getSummary().getValue();
                String str = event.getLocation().getValue().replaceAll("Manufacture", "PLG");
                String str2 = str.replaceAll("_", " - ");
                obj[i][4] = str2;
                obj[i][5] = null;
                i++;
            }
        }

        // Set Table
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
                obj,
                new String[]{
                    "Date", "DÃ©but", "Fin", "TitreCours", "Lieu", "OpenFiles"
                }
        ));
        jScrollPane1.setViewportView(jTable1);
        if (jTable1.getColumnModel().getColumnCount() > 0) {
            jTable1.getColumnModel().getColumn(0).setMinWidth(150);
            jTable1.getColumnModel().getColumn(0).setPreferredWidth(150);
            jTable1.getColumnModel().getColumn(0).setMaxWidth(150);
            jTable1.getColumnModel().getColumn(1).setMinWidth(60);
            jTable1.getColumnModel().getColumn(1).setPreferredWidth(60);
            jTable1.getColumnModel().getColumn(1).setMaxWidth(60);
            jTable1.getColumnModel().getColumn(2).setMinWidth(60);
            jTable1.getColumnModel().getColumn(2).setPreferredWidth(60);
            jTable1.getColumnModel().getColumn(2).setMaxWidth(60);
            jTable1.getColumnModel().getColumn(4).setMaxWidth(100);
            jTable1.getColumnModel().getColumn(5).setMaxWidth(100);
        }
    }
